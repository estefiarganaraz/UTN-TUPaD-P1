"""
Programa para decidir si una nota esta aprobada o no.
Si la nota es mayor o igual que 6, muestra "Aprobado".
En caso contrario, "Desaprobado".
Author= Estefania Nelson
Date= 6/4/2025
"""


nota = int(input("Ingrese su nota: "))

if nota >= 6:
    print("Aprobado")
else:
    print("Desaprobado")

